#include <stdio.h>

int main() {
    int a = 10, b = 3;
    
    printf("a = %d, b = %d\n", a, b);
    printf("a + b = %d\n", a + b);   // 鍔犳硶
    printf("a - b = %d\n", a - b);   // 鍑忔硶
    printf("a * b = %d\n", a * b);   // 涔樻硶
    printf("a / b = %d\n", a / b);   // 闄ゆ硶锛堟暣鏁伴櫎娉曪級
    printf("a %% b = %d\n", a % b);  // 鍙栦綑锛堟ā杩愮畻锛?    
    return 0;
}
